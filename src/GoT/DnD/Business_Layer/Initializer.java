/**
 * The first piece of code to run once the game will start
 */
public class Initializer {
    //Fields
    static GameUnit Hero;
    public static void initialize() {
        //TODO: get user input for CLI
        int PlayerSelection; //needs to get user input
        switch(PlayerSelection){
            case 1: Hero= new Warrior ("Jon Snow",300, 30,4,new Point(0,0),6);
                    break;
            case 2: Hero= new Warrior ("The Hound",400, 20,6,new Point(0,0),4);
                    break;
            case 3: Hero = new Mage("Melisandre",160,10,1,new Point(0,0),40,300,30,5,6);
                    break;
            case 4: Hero = new Mage("Thoros of Myr",250,25,3,new Point(0,0),15,150,50,3,3);
                    break;
            case 5: Hero = new Rogue("Arya Stark",150,40,2,new Point(0,0),20);
                    break;
            case 6: Hero = new Rogue("Bronn",250,35,3,new Point(0,0),60);
                    break;
        }
        new GameSystem(Hero);

    }
}
